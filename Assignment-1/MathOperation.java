class MathOperation {

    void calculate(int a, int b) {
        System.out.println("Addition: " + (a + b));
    }

    void calculate(double a, double b) {
        System.out.println("Multiplication: " + (a * b));
    }

    void calculate(int a) {
        System.out.println("Square: " + (a * a));
    }
}